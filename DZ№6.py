"""
Создать функцию наполнения списка путем ввода данных с клавиатуры.
"""
list = []
"""Создали лист для ввода данных"""
def creatList():
    """Создали функцию"""
    count = 4
    """Подключили счетчик"""
    while count != 0:
        """Запустили цикл ввода и записи данных"""
        element = input("Введите слово: ")
        list.append(element)
        """Добавляем данные в список"""
        count -= 1
        number = int(input("Введите число: "))
        list.append(number)
        count -= 1
    print(list)

if __name__ == '__main__':
    creatList()
"""Вызвали функцию"""
