"""	Создать функцию которая будет генерировать числовые значения в кол-ве 200 шт., затем вывести наибольшее и наименьшее значение из списка."""
import random
"""Импортировали модуль"""
def randomizerNumber():
    """Создали функцию"""
    count = 200
    """Создали счетчик количества чисел"""
    listNumber = []
    """Создали список"""
    while  count != 0:
        """Запустили цикл"""
        number = random.randrange(1, 10000, 5)
        """Создали переменную в диапазоне от 1 до 1000, шаг=5"""
        listNumber.append(number)
        """Записали переменные в список"""
        count -= 1
    print(listNumber)
    """Вывели список"""
    maxNumber = max(listNumber)
    minNumber = min(listNumber)
    """Создали переменные для max и min значений"""
    print("Наибольшее значение: ", maxNumber)
    print("Наименьшее значение: ", minNumber)
    """Вывели max и min значения"""

if __name__ == '__main__':
    randomizerNumber()
"""Вызвали функцию"""
